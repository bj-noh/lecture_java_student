package assignment07_02;

public class Rectangle {
	/*
	 *  Rectangle 클래스는 Shape 추상 클래스를 상속받으며,
	 * 멤버 변수와 메소드는 다음과 같이 구성된다.
	 * 
	 * - width: double
	 * 		직사각형의 너비
	 * 
	 * - height: double
	 * 		직사각형의 높이
	 * 
	 * + Circle(int x, int y, String color, double width double height)
	 * 		입력받은 x, y, color값을 상위 클래스인 Shape 클래스의 멤버에 할당하고, radius는 Circle 클래스의 멤버 변수 radius에 할당하는 생성자 
	 *  
	 * + getArea(): double
	 * 		 Rectangle의 면적을 반환하는 메소드
	 * 
	 * + getPerimeter(): double
	 * 		 Rectangle의 둘레를 반환하는 메소드. 
	 * 
	 * + setWidth(double width): void
	 * 		Rectangle의 너비를 입력받은 파라미터로 설정(변경)하는 메소드
	 * 
	 * + setHeight(double height): void
	 * 		Rectangle의 높이를 입력받은 파라미터로 설정(변경)하는 메소드
	 * 
	 * + getWidth(): double
	 * 		Rectangle의 너비를 반환하는 메소드
	 * 
	 * + getHeight(): double
	 * 		Rectangle의 높이를 반환하는 메소드
	 */	
}
